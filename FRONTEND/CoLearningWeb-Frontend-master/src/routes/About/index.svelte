                     <h2>THE NEED FOR THIS PROJECT</h2>
                      <div class="slog">
                     <h3>1.FREE LEARNING FROM THE MONOPOLY OF INSTITUTIONS</h3>
                     <p>The prevalent educational institutes which follow the factory schooling approach are engaged in the process of selling us packaged knowledge in the name of education. Overtime a learner becomes a consumer of knowledge and loses his capability to create his own knowledge based on his own experience and reasoning. The institutes become the primary sources of knowledge for these learners. This may lead to manipulation and enslavement of the learner throughout his lifetime.
                                <br>
                      Example-<br>
                      In the name of progress and economic growth what was earlier freely accessible to us is now sold back to us in the form of services, e.g. learning as education, growing our own natural food as chemical farming, self care as health services, community singing and dance as entertainment, child care as day care services, caring for our old ones into care giving services etc.</p>
                    </div>
                      <div class="slog">
                    <h3>2.REMOVAL OF BARRIERS TO LEARNING DUE TO PREREQUISITE QUALIFICATIONS</h3>
                    
                    <p>Many times prerequisite of qualifications in the form of degrees and certificates from lower institutions keeps many a passionate learners away from what he wants to learn. Sometimes such hierarchies and dependencies are so strong that many forms of learning may become virtually inaccessible for those who have not followed the strict path of prequalification decided by Institutes.
                        
                      <br>Example-
                      <br> Nutrition cannot be applied for let alone studied by those who lack a science degree.
                    </p>
                  
                  </div>
                  <div class="slog">

                    <h3>3.REINFORCING THE IMPORTANCE OF PEOPLE WITH PRACTICAL KNOWLEDGE AND WISDOM.</h3>
                    <p>In many cases people with practical knowledge about the subject by virtue of hands on work experience in the field can add tremendous value to a learner as compared to an institute that is focused on academics. This connection between learners and practical storehouses of knowledge is crucial.
                       <br>
                      <br> Also in such cases, the knowledge passed on is relevant and current.
                    </p>
                  </div>
                  <div class="slog">
                    <h3>4.PRESERVATION OF INDIGENOUS AND LOCAL KNOWLEDGE.</h3>
                    <p>In the name of globalization and scalability, as communities get disintegrated and diversity is compromised for the sake of scalability by mono-culture, a lot of indigenous and local wisdom is at the risk of getting lost due of lack of connections between people who have knowledge of the same and people who may be interested in carrying it forward. Preservation of indigenous wisdom and diversity is crucial for long term sustainability.

                    </p>
                  </div>

                <div class="slog">
                    <h3>5.PLATFORM FOR EXCHANGE OF SUSTAINABLE WAYS OF LEARNING AND LIVING.</h3>
                    <p>Many experiments and projects are happening which are interested in creating sustainable options of learning and living parallel to the mainstream ideology of mono culture. A connection of all these learning societies that nurture the human spirit.
                           <br>
                      Example-
                      <br>All learners coming to programs like LSUC can be networked through this web. This will help collaboration and mutual support.
                    </p>
                  </div>
                  <div class="slog">
                    <h3>6.MENTOR-SHIP FOR SELF DIRECTED LEARNERS.</h3>
                    <p>Many learners take the path of self directed learning to walk their own paths of learning and living as they believe that, the world is the best classroom and it offers them greater autonomy in their learning process. These learners can greatly benefit if they are able to connect to mentors.
                    </p>
                  </div>
                  <div class="slog">
                    <h3>7.REMOVAL OF FINANCIAL CONSTRAINTS FOR LEARNING.</h3>
                    <p>Many institutes just view education as a business with the sole intention of profits. By connecting learners to a web of gurus, many learners get access to knowledge despite financial constraints.
                    </p>
                  </div>
                  <div class="slog">
                    <h3>8.CONNECTION TO CO-LEARNERS.</h3>
                    <p>Individual learners on their own journeys can greatly benefit if they are able to connect to peers and co-learners on similar paths for joint exploration of common interests. Need of the Human touch in learning. There is no dearth of information in the form of books, web content, video tutorials etc. . No doubt all this act as great learning resources. Direct connection between the learner and the Guru will help take learning to new heights by leveraging active personalized engagement between the learner and Guru and by building life long relationships.
                    </p>
                  </div>
                  <div class="slog">
                    <h3>9.HUMANITY.</h3>
                    <p>Finally, losing our natural ability of learning and thinking and our collective wisdom to institutions is a great loss to humanity. Learners Web will free learning from these institutions and give it back in the hands of humans. Each human will be a centre of sharing and learning. There are lot of individuals who would like to gift their knowledge and skills to earnest learners before they leave this world.
                    </p>
